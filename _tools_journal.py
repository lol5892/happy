MAX = 1100
n = 0
missing = []
parts = []
for title, items in chapters:
    cards = []
    for orig, caption, badge in items:
        src = os.path.join(ROOT, orig)
        if not os.path.exists(src):
            missing.append(orig)
            continue
        n += 1
        dstname = f"journal-{n:02d}.jpg"
        im = Image.open(src)
        im = ImageOps.exif_transpose(im).convert("RGB")
        im.thumbnail((MAX, MAX))
        im.save(os.path.join(DST, dstname), "JPEG", quality=85, optimize=True)
        alt = html.escape(caption)
        badge_html = f'\n          <span class="card-badge">{html.escape(badge)}</span>' if badge else ""
        cards.append(
            f'''        <figure class="card reveal">
          <div class="card-img"><img src="assets/photos/journal/{dstname}" alt="{alt}" loading="lazy" />{badge_html}</div>
          <figcaption>{html.escape(caption)}</figcaption>
        </figure>'''
        )
    parts.append(
        f'''      <div class="chapter">
        <h3 class="chapter-title"><span>{html.escape(title)}</span></h3>
        <div class="cards">
{chr(10).join(cards)}
        </div>
      </div>'''
    )

journal_html = "\n\n".join(parts)
open(os.path.join(ROOT, "_journal.html"), "w", encoding="utf-8").write(journal_html)

print("Photos processed:", n)
print("Missing:", missing if missing else "none")
