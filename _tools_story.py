# Сохраняем фото и собираем HTML таймлайна
tl_items = []
for i, (orig, title, text, badge) in enumerate(story, 1):
    dst = f"story-{i:02d}.jpg"
    save(orig, dst)
    tl_items.append(
        f'''      <article class="tl-item reveal">
        <div class="tl-photo">
          <img src="assets/photos/{dst}" alt="{html.escape(title)}" loading="lazy" />
          <span class="tl-badge">{html.escape(badge)}</span>
        </div>
        <div class="tl-text">
          <h3>{html.escape(title)}</h3>
          <p>{html.escape(text)}</p>
        </div>
      </article>'''
    )
open(os.path.join(ROOT, "_story.html"), "w", encoding="utf-8").write("\n\n".join(tl_items))

